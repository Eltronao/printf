#include<unistd.h>

int	ft_putunbr_n(unsigned int num, int *n)
{
	char	m;

	if (num > 9)
	{
		ft_putunbr_n(num / 10, n);
		m = (num % 10) + '0';
		write(1, &m, 1);
		*n += 1;
	}
	else
	{
		m = num + '0';
		write(1, &m, 1);
		*n += 1;
	}
	return (*n);
}
